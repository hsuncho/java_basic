package self_study;

public class DataTypeEX01 {

	public static void main(String[] args) {

		System.out.println("1. 정수 리터럴 연습");
		int var1 = 0b1011;
		int var2 = 0206;
		int var3 = 365;
		int var4 = 0xB3;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
		System.out.println("================");
		System.out.println("2. long 타입 변수");
		long a = 10;
		long b = 20L;
		long c = 100000000000000000L;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println("================");
		System.out.println("3. 소수점이 있는 실수형");
		double d = 3e6;
		float e = 3e6f;
		double f = 2e-3;
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		
		
	}

}
